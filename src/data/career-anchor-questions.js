// 计算职业锚点得分
export const calculateCareerAnchorScores = (answers) => {
  // 按类型分组题目得分
  const typeScores = {}
  Object.keys(typeToQuestionMap).forEach(type => {
    typeScores[type] = typeToQuestionMap[type].map(qId => answers[qId] || 0)
  })
  
  // 找出所有题目中得分最高的三个
  const allScoresWithId = Object.entries(answers).map(([id, score]) => ({
    id: parseInt(id),
    score
  })).filter(item => item.score > 0)
  
  // 按分数降序排序
  allScoresWithId.sort((a, b) => b.score - a.score)
  
  // 取前三名最高分的题目ID
  const topThreeIds = allScoresWithId.slice(0, 3).map(item => item.id)
  
  // 计算每种类型的总分，并为三个最高分题目加4分
  const finalScores = {}
  Object.keys(typeScores).forEach(type => {
    let totalScore = 0
    typeToQuestionMap[type].forEach(qId => {
      let score = answers[qId] || 0
      // 如果是最高分题目之一，额外加4分
      if (topThreeIds.includes(qId)) {
        score += 4
      }
      totalScore += score
    })
    finalScores[type] = totalScore
  })
  
  return finalScores
} 